package com.capgemini.builder;

/**
 * Class Bottle
 */
public class Bottle extends Packaging {

    /**
     * Default constructor
     */
    public Bottle() {
    }

    /**
     * Bottle Name
     */
    public String name = "Bottle";
    
    
    
    /**
     * Accessors
     */
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
    
    

}